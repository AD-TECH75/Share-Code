/* untuk menampilkan tulisan di halaman web
   dengan komentar lebih dari satu baris */
document.writeln("<p>Hello World comment in more line</p>");

// belajar variabel
document.writeln("Hello World"); // tipe data string
document.writeln("<br>"); // untuk ganti baris
document.writeln(100); // tipe data number
document.writeln("<br>");
document.writeln(3.14); // tipe data float
document.writeln("<br>");
document.writeln(5 + 5); // operasi matematika
document.writeln("<br>");
document.writeln(0xff); // tipe data hexadecimal
document.writeln("<br>");
document.writeln(true); // tipe data boolean true
document.writeln("<br>");
document.writeln(false); // tipe data boolean false
document.writeln("<br>");
document.writeln(0b100111); // tipe data binary
document.writeln("<br>");
document.writeln(0o123); // tipe data octal
document.writeln("<br>");
document.writeln("100" + 100); // tipe data string dan number
document.writeln("<br>");
document.writeln(
	"hasil 1+1" + "= " + (1 + 1)
); /* tanda tambah dapat digunakan untuk menggabungkan string atau untuk operasi matematika */
